let split_kind kind =
  match kind with
  | UNIMPL -> [ UNIMPL ]
  | V1C1 -> [ V1; C1 ]
  | V1 -> [ V1 ]
  | C1 -> [ C1 ]
  | V1E1 -> [ V1; E1 ]
  | E1 -> [ E1 ]
  | B1 -> [ B1 ]
  | B1V1 -> [ B1; V1 ]
  | CV -> [ CV ]
  | V1V2 -> [ V1; V2 ]
  | V2 -> [ V2 ]
  | V1V2B1 -> [ V1; V2; B1 ]
  | B1B2B4V1V2 -> [ B1; B2; B4; V1; V2 ]
  | B2 -> [ B2 ]
  | B4 -> [ B4 ]
  | B1B2B4V1 -> [ B1; B2; B4; V1 ]
  | B1V2V3V4 -> [ B1; V2; V3; V4 ]
  | V3 -> [ V3 ]
  | V4 -> [ V4 ]
  | VVB1C1 -> [ VV; B1; C1 ]
  | VV -> [ VV ]
  | V2V3V1 -> [ V2; V3; V1 ]
  | V1V2B1V3 -> [ V1; V2; B1; V3 ]
  | V1B2B4V2 -> [ V1; B2; B4; V2 ]
  | B1V1V2 -> [ B1; V1; V2 ]
  | Empty -> [ Empty ]

